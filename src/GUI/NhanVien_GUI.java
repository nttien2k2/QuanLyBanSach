    private void btnThemMoiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMoiMouseClicked
        // TODO add your handling code here:
        ArrayList<JTextField> l_TextFields = this.l_TextF();
        for(JTextField textF : l_TextFields){
            if(textF.getText().equals("") || Picker_ngaySinh.getDate().equals("")){
                JOptionPane.showConfirmDialog(this, "Hãy nhập đầy đủ dữ liệu", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        
        String MaNV = Text_maNV.getText();
        String hoTen = Text_tenNV.getText();
        String gioiTinh = radioNam.isSelected() ? radioNam.getText() : radioNu.getText();
        String email = Text_Email.getText();
        String sdt = Text_sdt.getText();
        String cmnd = Text_cmnd.getText();
        String diaChi = Text_DiaChi.getText();
        String chucVu = Cbb_chucVu.getSelectedItem().toString();
        String ngaySinh = new SimpleDateFormat("yyyy-MM-dd").format(Picker_ngaySinh.getDate());
        
        int resultCheck = NhanVien_DAO.checkExistNhanVien(email, sdt, cmnd);
   
        if(resultCheck == 0){
            int result = NhanVien_DAO.addNewNhanVien(MaNV, hoTen, ngaySinh, gioiTinh, email, sdt, cmnd, diaChi, chucVu);
            if(result >= 1){
                JOptionPane.showConfirmDialog(this, "Thêm mới nhân viên thành công !!!", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
                this.loadTableNhanVien();
                return;
            }
            
            JOptionPane.showConfirmDialog(this, "Có vẻ như lỗi thêm nhân viên !!!", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE);
            return;
        }
        else{
            if(resultCheck == -1){
                JOptionPane.showConfirmDialog(this, "Có vẻ như chương trình bị lỗi !!!", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        JOptionPane.showConfirmDialog(this, "Email, sdt, cmnd không được khớp bất kì nhân viên nào !", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnThemMoiMouseClicked

    private void Text_sdtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Text_sdtKeyPressed
        // TODO add your handling code here:
        if(Character.isLetter(evt.getKeyChar()) || Text_sdt.getText().length() == 11 && evt.getKeyChar() != KeyEvent.VK_BACK_SPACE){
            Text_sdt.setEditable(false);
            return;
        }
        Text_sdt.setEditable(true);
        Text_sdt.setCaretPosition(Text_sdt.getText().length());
        
    }//GEN-LAST:event_Text_sdtKeyPressed

