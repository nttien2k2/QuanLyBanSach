    private void Table_nhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_nhanVienMouseClicked
        // TODO add your handling code here:
        if(Table_nhanVien.getSelectedRowCount() == 1){
            ArrayList<String> valuesColumn = new ArrayList<String>();
            int numRow = Table_nhanVien.getSelectedRow();
            for(int i = 0; i < Table_nhanVien.getColumnCount(); i++){
                String value = Table_nhanVien.getValueAt(numRow, i).toString();
                valuesColumn.add(value);
            }

            Text_maNV.setText(valuesColumn.get(0));
            Text_maNV.setFocusable(false);
            Text_tenNV.setText(valuesColumn.get(1));
            try {
                Picker_ngaySinh.setDate(new SimpleDateFormat("dd/MM/yyyy").parse(valuesColumn.get(2)));
            } catch (ParseException ex) {}

            if(valuesColumn.get(3).equals("Nam")){
                radioNam.setSelected(true);
                radioNu.setSelected(false);
            }
            else{
                radioNu.setSelected(true);
                radioNam.setSelected(false);
            }

            Text_Email.setText(valuesColumn.get(4));
            Text_sdt.setText(valuesColumn.get(5));
            Text_cmnd.setText(valuesColumn.get(6));
            Text_DiaChi.setText(valuesColumn.get(7));
            Cbb_chucVu.setSelectedItem(valuesColumn.get(8));
           
        }
    }//GEN-LAST:event_Table_nhanVienMouseClicked

