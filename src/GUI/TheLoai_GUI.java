    private void addColumnTableTheLoai(){
        DefaultTableModel tableModel = (DefaultTableModel)table_theLoaiSach.getModel();
        
        tableModel.setColumnIdentifiers(new String[]{
            "Mã thể loại", "Thể loại"
        });
        
        DefaultTableColumnModel tableColumnModel = (DefaultTableColumnModel)table_theLoaiSach.getColumnModel();
        tableColumnModel.getColumn(0).setPreferredWidth(200);
        tableColumnModel.getColumn(1).setPreferredWidth(200);
        
        table_theLoaiSach.getTableHeader().setFont(new Font("Arial", Font.BOLD, 15));
    }
    
    private void loadTableTheLoai(){
        this.addColumnTableTheLoai();
        
        DefaultTableModel tableModel = (DefaultTableModel)table_theLoaiSach.getModel();
        tableModel.setRowCount(0);
        this.l_theLoais = TheLoai_DAO.getListTheLoai();
        
        for(TheLoai_DTO item : this.l_theLoais){
            tableModel.addRow(new String[]{
                item.getMa_TL(),
                item.getTl_Sach()
            });
        }
    }

