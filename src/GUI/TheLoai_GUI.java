    private void btn_ThemTheLoaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ThemTheLoaiMouseClicked
        // TODO add your handling code here:
        if(text_maTL.getText().equals("") || text_TL.getText().equals("")){
            JOptionPane.showConfirmDialog(this, "Hãy nhập đầy đủ dữ liệu", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(JOptionPane.showConfirmDialog(this, "Thêm mới thể loại ?", "Thông báo", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.OK_OPTION){
            int result = TheLoai_DAO.addNewTheLoai(text_maTL.getText(), text_TL.getText());
            this.loadTableTheLoai();
            if(result <= 0){
                JOptionPane.showConfirmDialog(this, "Có vẻ như xảy ra lỗi !!!", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_ThemTheLoaiMouseClicked

    private void btn_suaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_suaMouseClicked
        // TODO add your handling code here:
        if(table_theLoaiSach.getSelectedRowCount() == 1){
            if(JOptionPane.showConfirmDialog(this, "Đồng ý sửa thể lại này ?", "Thông báo", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.OK_OPTION){
                int result = TheLoai_DAO.updateTheLoai(text_maTL.getText(), text_TL.getText());
                this.loadTableTheLoai();
                
                if(result <= 0){
                    JOptionPane.showConfirmDialog(this, "Có vẻ như xảy ra lỗi !!!", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        else{
            JOptionPane.showConfirmDialog(this, "Hãy chọn thể loại muốn sửa", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_suaMouseClicked

    private void btn_xoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_xoaMouseClicked
        // TODO add your handling code here:
        if(table_theLoaiSach.getSelectedRowCount() == 1){
            if(JOptionPane.showConfirmDialog(this, "Đồng ý xoá thể lại này ?", "Thông báo", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.OK_OPTION){
                int result = TheLoai_DAO.deleteTheloai(text_maTL.getText());
                this.loadTableTheLoai();
                
                if(result <= 0){
                    JOptionPane.showConfirmDialog(this, "Có vẻ như xảy ra lỗi !!!", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        else{
            JOptionPane.showConfirmDialog(this, "Hãy chọn thể loại muốn xoá", "Thông báo", JOptionPane.CLOSED_OPTION, JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_xoaMouseClicked

    private void text_searchTheLoaiKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_text_searchTheLoaiKeyReleased
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel)table_theLoaiSach.getModel();
        
        tableModel.setRowCount(0);
        
        for(TheLoai_DTO item : this.l_theLoais){
            if(item.getTl_Sach().contains(text_searchTheLoai.getText())){
                tableModel.addRow(new String[]{
                    item.getMa_TL(),
                    item.getTl_Sach()
                });
            }
        }    
    }//GEN-LAST:event_text_searchTheLoaiKeyReleased

  
    
